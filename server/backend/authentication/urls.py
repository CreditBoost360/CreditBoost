from django.urls import path, re_path
from .views import (
    CustomProviderAuthView,
    CustomTokenObtainPairView,
    CustomTokenRefreshView,
    CustomTokenVerifyView,
    LogoutView,
    BusinessDetailCreateView,BusinessDetailRetrieveUpdateView
)

urlpatterns = [
    re_path(
        r'^o/(?P<provider>\S+)/$',
        CustomProviderAuthView.as_view(),
        name='provider-auth'
    ),
    path('jwt/create/', CustomTokenObtainPairView.as_view()),
    path('jwt/refresh/', CustomTokenRefreshView.as_view()),
    path('jwt/verify/', CustomTokenVerifyView.as_view()),
    path('logout/', LogoutView.as_view()),
    
    path('business-details/', BusinessDetailCreateView.as_view(), name='business-details-create'),
    path('business-details/update/', BusinessDetailRetrieveUpdateView.as_view(), name='business-details-retrieve-update'),
    
]